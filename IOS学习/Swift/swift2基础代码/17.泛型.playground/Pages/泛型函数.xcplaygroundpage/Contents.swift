//: 泛型函数: 可以用匹配任何类型

//:  - 用一个任意名称的占位符,取代原先实际类型.泛型函数名后加<占位符>.
//打印任意数组元素
func 打印任意数组元素<类型占位符>(数组: [类型占位符]) {
    for 元素 in 数组 {
        print(元素)
    }
}

